package com.briup.knn;

public class AvgNum {
	private int number;
	private double avg;

	public AvgNum() {
		super();
	}

	public AvgNum(int number, double avg) {
		super();
		this.number = number;
		this.avg = avg;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public double getAvg() {
		return avg;
	}

	public void setAvg(double avg) {
		this.avg = avg;
	}

}
